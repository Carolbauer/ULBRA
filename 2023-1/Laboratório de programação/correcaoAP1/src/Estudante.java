public class Estudante {
    private String nome;
    private int idade;
    private int pontos;

    public String getNome(){
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade(int i) {
        return idade;
    }

    public void setPontos(int pontos) {
        this.pontos = pontos;
    }

    public int getPontos(int i) {
        return pontos;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}
